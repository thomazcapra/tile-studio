// Pack a 0xRRGGBB color into the renderer's AABBGGRR layout with full alpha.
export function packPreset(rgb: number): number {
  const r = (rgb >> 16) & 0xff;
  const g = (rgb >> 8) & 0xff;
  const b = rgb & 0xff;
  return ((0xff << 24) | (b << 16) | (g << 8) | r) >>> 0;
}
